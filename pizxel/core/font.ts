/**
 * Font class for rendering text
 */
export class Font {
  private charset: Record<string, CharBitmap>;
  charWidth: number = 8;
  charHeight: number = 8;

  constructor() {
    this.charset = ZX_SPECTRUM_FONT;
  }

  /**
   * Get character bitmap
   */
  getChar(char: string): CharBitmap | null {
    return this.charset[char] || null;
  }

  /**
   * Check if character exists in font
   */
  hasChar(char: string): boolean {
    return char in this.charset;
  }

  /**
   * Add custom character to font
   */
  addChar(char: string, bitmap: CharBitmap): void {
    if (bitmap.length !== 8) {
      throw new Error("Character bitmap must be 8 bytes");
    }
    this.charset[char] = bitmap;
  }
}

